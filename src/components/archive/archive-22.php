    <h2>Kapitel 19 (November 2016 - April 2018)<span class="arrow-left jsdep"></span></h2>
    <p>Team B reist weiter nach Westen in Richtung Lyn´knoll & Edinmire. Sie ruhen sich für die Nacht an einer heißen Quelle aus. Trace und Co. werden zum Legacy Manor eskortiert. Sie treffen Prinzessin Reni wieder und sie liest seine Gedanken. Eine ältere Roselyn entpuppt sich als die Verwalterin des Anwesens. Nachdem Edinmires Gefängnis zerstört wurde, werden die gefangenen Wölfe in die Kerker des Herrenhauses gebracht. Team A ruht sich etwas aus, während Natani und Keith etwas Zeit für sich haben. "Willst du sie anfassen?" Am nächsten Morgen gibt es Ärger für Team B in Form von Carver! Clovis, Carvers Boss, wird angedeutet. Carver wird besiegt, aber Zen wird verletzt. Reni eilt Team B zu Hilfe, und die Gruppen finden endlich wieder zueinander! Während sie sich von dem Flug erholt, beschreibt Reni ihre Abstammung und die Drachenhierarchie. Die Gruppe kehrt per Gatekey zurück. Euchre gibt Rose eine Warnung. Carver´s Schicksal.</p>
