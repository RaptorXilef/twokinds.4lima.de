    <h2>Kapitel 6, Teil 2 (Januar 2005 - Juni 2005)<span class="arrow-left jsdep"></span></h2>
    <p>Flora wird von Ephemeral <a href="20050131.php">besessen</a>, der ihren Körper benutzt, um die Gruppe zu <a href="20050304.php">bekämpfen</a>. Trace beginnt ein zweites Mal, <a href="20050325.php">die Kontrolle zu verlieren</a>, aber <a href="20050404.php">Euchre schreitet ein</a>. Trace verbindet sich geistig mit Flora, was Ephemerals Macht so weit schwächt, dass sie <a href="20050425.php">gebrochen</a> werden kann. Es stellt sich heraus, dass die Hafenstadt eine riesige, von der Maske gestellte <a href="20050525.php">Illusionsfalle</a> war, und <a href="20050606.php">Saria verabschiedet sich</a>.</p>
