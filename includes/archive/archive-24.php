    <h2>Kapitel 21 (März 2020 - Januar 2023)<span class="arrow-left jsdep"></span></h2>
    <p>Trace und Flora werden in den geheimen Korridor teleportiert und treffen dort auf Stoney! Er ist freundlich. Währenddessen wird die Gruppe in der Oase von Traces anderen Versuchen, Steinwächter zu werden, angegriffen. Dunkle Geheimnisse aus Trace´ Vergangenheit werden aufgedeckt. Saria war schwanger, als sie starb, und Rose ist nicht das, was sie zu sein scheint. Während des Chaos bricht Clovis in das Anwesen ein. Seine Männer greifen Sythe an und sind hinter Zen her. Im Gewächshaus, zwischen verdrehten Rosenranken, hat Detritus seinen Auftritt. Er plant, Saria mit Hilfe von Marens Körper wieder zum Leben zu erwecken. Reni bricht aus dem Oasenraum aus und befreit die anderen. Clovis betritt den Kerker und schaltet seine eigenen Männer aus, um scheinbar lose Enden zu schließen. Nibbly verteidigt einen verwundeten Sythe und führt ihn dann zu einem versteckten Gang. Clovis und Reni kämpfen. Kat wird von Clovis´ anderem Wolf angegriffen, bekommt aber Hilfe von Zen und Eric. Stoney kämpft gegen Detritus und rettet den Tag!</p>
