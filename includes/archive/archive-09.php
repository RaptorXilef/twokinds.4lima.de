    <h2>Kapitel 7 (Juni 2005 - März 2006)<span class="arrow-left jsdep"></span></h2>
    <p>Die Gruppe reist in den Süden nahe der Wolfsgrenze, um die echte Hafenstadt Wreathwood zu erreichen. Trace und Flora verbringen ihre <a href="20050706" >erste gemeinsame Nacht</a>, und die Masken <a href="20050715" >mischen sich wie üblich ein</a>. Wolfs-Attentäter <a href="20050803" >greifen an</a>! Trace wendet zum ersten Mal versehentlich <a href="20050917" >schwarze Magie</a> an und beschwört einen Dämon, der den Kampf beendet. Die Drachendame, <a href="20051007" >Lady Nora</a>, hat ihren ersten Auftritt, sehr zum Entsetzen aller.</p>
