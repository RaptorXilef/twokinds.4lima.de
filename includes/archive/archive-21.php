    <h2>Kapitel 18 (Februar 2015 - November 2016)<span class="arrow-left jsdep"></span></h2>
    <p>Team A landet schließlich in Floras altem Heimatdorf. Keith ist entschlossen, Natani zu heilen. Der Krieg mit den Wölfen eskaliert. Flora trifft sich wieder mit ihrem alten `Bruder´ Kei. Sie vereinbaren ein Treffen in der Taverne von Riftwall, wo Adira und Maeve zum ersten Mal auftauchen! Es findet ein Tigertreffen statt. Keith und Co. kämpfen gegen Söldner auf der Suche nach Wölfen, angeführt von Saraphina. Der Kampf endet durch die Drachenprinzessin. Das Tigertreffen beginnt... es ist eine Falle! Währenddessen kämpft Natani gegen ihre inneren Dämonen.</p>
