<h2 class="page-header">FAQ</h2>
	<p>
		<b>Benachrichtigung:</b> Erhalte eine E-Mail, wenn ich eine neu übersetzte Comicseite hochlade.<br>
		<br><b>Warum?</b> Durch meine Arbeit und Familientätigkeiten komme ich nur unregelmäßig zum Übersetzen. Damit du nicht täglich nachsehen musst, ob eine neue Seite hochgeladen wurde, möchte ich die diese Möglichkeit bieten.<br>
		<br><b>Wie aktivieren?</b> Trage in dem Formularfeld oben deine E-Mail-Adresse ein und drücke auf Benachrichtigung "Abonnieren", das war es schon, so einfach geht es.<br>
        <br><b>Wie deaktivieren?</b> Trage in dem Formularfeld oben deine E-Mail-Adresse ein und drücke auf Benachrichtigung "Abbestellen", das war es schon.<br>
        <br><b>Verwendung meiner Daten?</b> Deine E-Mail-Adresse wird gut geschützt gespeichert. Immer wenn ich eine neue Seite hochlade, übergebe ich alle E-Mail-Adressen in den Blind Carbon Copy (BCC) Bereich meines E-Mail-Klienten (Outlook) und sende dir eine kurze Info zum Upload, ggf. mit einer Vorschau der Seite. <br>
        <br><b>Warum Blind Carbon Copy (BCC)</b> Ganz einfach, damit alle anderen E-Mail-Empfänger deine E-Mail-Adresse nicht sehen. (Datenschutz)<br>
        <br><b>Was passiert noch mit meinen Daten?</b> Nichts! Die E-Mail-Adresse wird ausschließlich zum Senden der Benachrichtigung beim Hochgeladen neu übersetzter Comicseiten genutzt. Sie wird weder an Dritte weitergegeben noch anderweitig verwendet oder verarbeitet. Bestellst du die Benachrichtigung wieder ab, wird deine E-Mail-Adresse automatisch und endgültig aus dem System gelöscht, bis du sie wieder einträgst!<br>
        




